<template>
  <div class="card overflow-hidden" v-if="data">
    <ratio-visual :data="{ visual: data.visuals ? data.visuals[0] : undefined, name: data.title }" />
    <nuxt-link :to="`/app/detail/${data.id}`" class="card-body">
      <h5 class="card-title">{{ data.title }}</h5>

      <p v-if="data.available">
        To {{ data.type }}

        <span v-if="data.type === 'rent' && data.rent" class="opacity-50">Ä§{{ data.rent.start }}</span>
      </p>
      <p v-else>Not available</p>
    </nuxt-link>

    <div class="card-footer" v-if="data.name">
      <div class="d-flex gap-2 align-items-center">
        <button class="bg-light p-2 rounded-circle lh-1 text-white" @click="showModal()">
          <i class="bi bi-chat-text-fill"></i>
        </button>
        <div>
          <nuxt-link class="lh-1" :to="`/app/user/${data.id}`">{{ data.name }}</nuxt-link>
          <div class="lh-sm">
            {{ data.neighborhood }} <span class="opacity-75" v-if="data.distance && place"> {{ data.distance }} km</span>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
export default {
  props: {
    data: {
      type: [Object, Array],
      default: () => {},
    },
  },

  computed: {
    place() {
      return this.$store.state.search.place;
    },
  },

  methods: {
    showModal() {
      if (this.$store.state.user.signedIn) {
        this.$store.commit("modals/show", { name: "request", data: this.data });
      } else {
        this.$store.commit("modals/show", { name: "connect" });
      }
    },
  },
};
</script>

<style lang="scss" scoped>
.card {
  height: 100%;
}
</style>
